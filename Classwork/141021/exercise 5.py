import math
a = int(input("Ведите первое число:"))
b = int(input("Введите второе число:"))
print("Сумма чисел равна:", a + b)
print("Разница между числами равна:", a - b,)
print("Произведение этих чисел равно:", a * b)
print("Частное от деления одного числа на другое равно:", a / b)
print("Остаток от деления одного числа на другое равно:", a % b)
print("Десятичный логарифм первого числа равен:", math.log10(a))
print("Результат возведения первого числа в степень второго числа:", a ** b)