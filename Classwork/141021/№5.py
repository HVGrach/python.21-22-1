import math

a = int(input("Введите целое число a:"))
b = int(input("Введите целое число b:"))
print("Сумма", a + b,
      "\nРазница", abs(a - b),
      "\nПроизведение", a * b,
      "\nЧастное", a / b,
      "\nОстаток от деления", a % b,
      "\nДесятичный логарифм", math.log10(a),
      "\nРезультата возведения числа a в степень b", a ** b)
